    private void crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActionPerformed

        ControlMaquinas_Aseo control=new ControlMaquinas_Aseo();
        boolean rs =control.insertarMaquinasAseo(listaMaquina);
        if(rs){
            JOptionPane.showMessageDialog(rootPane, "Se insertaron exitosamente");
            listaMaquina.clear();
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se insertaron los datos");
        }

    }//GEN-LAST:event_crearActionPerformed

