        String nombreP= nombreM.getText();
        String fechaLL = fechaL.getText();
        String fechaP = fechaS.getText();

        String idTM= String.valueOf(combo.getSelectedItem());
        String[] splt= idTM.split(" - ");

        int idTipoMaquina= Integer.parseInt(splt[0]);
        System.out.println("ser√°? : "+idTipoMaquina);

        maquinas_de_aseo maquina = new maquinas_de_aseo(idTipoMaquina, nombreP, fechaLL, fechaLL);
        

        boolean b = listaMaquina.add(maquina);
        System.out.println("es? : "+ b);

        //reset
        nombreM.setText("");
        fechaL.setText("");
        fechaS.setText("");

        //numero ++

        num++;
        numero.setText(String.valueOf(num));

