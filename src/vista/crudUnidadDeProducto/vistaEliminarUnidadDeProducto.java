    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
        
        String item = String.valueOf(combo.getSelectedItem());
        String[] sItem= item.split(" - ");
        int idProducto= Integer.parseInt (sItem[0]);
        
        ControlProducto_Unidad control = new ControlProducto_Unidad();
        
        control.deleteProductoUnidad(idProducto);
        
        combo.removeItem(combo.getSelectedItem()); 
        
        
        
        
        
    }//GEN-LAST:event_eliminarActionPerformed

