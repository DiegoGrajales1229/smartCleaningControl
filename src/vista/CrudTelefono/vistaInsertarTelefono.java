    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
       
        ControlEmpleado objCE = new ControlEmpleado();

        listaE = objCE.consultarEmpleados();

        for (int i = 0; i < listaE.size(); i++) {
            empleado objetoE = listaE.get(i);
            jComboBox1.addItem(objetoE.getNombre1Empleado()+""+objetoE.getNombre2Empleado()+""+objetoE.getApellido1Empleado()+""+objetoE.getApellido2Empleado());
        }
        
        
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        ControlTelefono objCT = new ControlTelefono();
        boolean t=objCT.insertarTelefono(listaT);
        
        if(t==true){
            JOptionPane.showMessageDialog(rootPane, "Se insertaron los telefonos");
            listaT.clear();
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se insertaron los telefonos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String telefono = tx1.getText();
        
         ControlEmpleado objce = new ControlEmpleado();
         
        String idEmpleado1="";
        for (int i = 0; i < listaE.size(); i++) {
            empleado get = listaE.get(i);
            String empleados=String.valueOf(jComboBox1.getSelectedItem());
            if(empleados.equals(get.getNombre1Empleado()+""+get.getNombre2Empleado()+""+get.getApellido1Empleado()+""+get.getApellido2Empleado())){
                idEmpleado1=get.getIdEmpleado();
            }
        }
        telefono objT = new telefono(telefono, idEmpleado1);
        

        listaT.add(objT);
        jComboBox1.setSelectedIndex(0);
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

